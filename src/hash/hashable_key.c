#include "libft.h"

size_t			hashable_key(const char *key)
{
	size_t		r;
	size_t		g;

	r = 0;
	while (*key)
	{
		r <<= 4;
		r += *key++;
		g = r & (0xF << (32 - 4));
		if (!g)
		{
			r ^= g >> (32 - 8);
			r ^= g;
		}
	}
	return (r);
}
